#!/usr/bin/env node
/**
 * Build-time changelog ingestion: reads thunderstore CHANGELOG.md and emits changelog.ts.
 * Run via npm prebuild/predev; source is copied from thunderstore/ by build-webdashboard.sh.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { marked } from 'marked';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const WEB_ROOT = path.resolve(__dirname, '..');
const CHANGELOG_SRC = path.join(WEB_ROOT, '.changelog-src', 'CHANGELOG.md');
const CHANGELOG_FALLBACK = path.resolve(WEB_ROOT, '../../thunderstore/CHANGELOG.md');
const VERSION_SRC = path.join(WEB_ROOT, '.changelog-src', 'Version.cs');
const VERSION_FALLBACK = path.resolve(WEB_ROOT, '../../Version.cs');
const OUT_DIR = path.join(WEB_ROOT, 'src/lib/generated');
const OUT_FILE = path.join(OUT_DIR, 'changelog.ts');

marked.setOptions({ gfm: true, breaks: false });

function resolveChangelogPath() {
  if (fs.existsSync(CHANGELOG_SRC)) return CHANGELOG_SRC;
  if (fs.existsSync(CHANGELOG_FALLBACK)) return CHANGELOG_FALLBACK;
  return null;
}

function readModuleVersion() {
  const versionPath = fs.existsSync(VERSION_SRC) ? VERSION_SRC : VERSION_FALLBACK;
  if (!fs.existsSync(versionPath)) return '';
  const content = fs.readFileSync(versionPath, 'utf8');
  const match = content.match(/ModuleVersion\s*=\s*"([^"]+)"/);
  return match ? match[1].trim() : '';
}

function rewriteExternalLinks(html) {
  return html.replace(
    /<a href="(https?:\/\/[^"]+)"/g,
    '<a href="$1" target="_blank" rel="noopener noreferrer"',
  );
}

function main() {
  const changelogPath = resolveChangelogPath();
  if (!changelogPath) {
    console.error(`error: changelog source not found at ${CHANGELOG_SRC}`);
    console.error(`fallback also missing: ${CHANGELOG_FALLBACK}`);
    process.exit(1);
  }

  const markdown = fs.readFileSync(changelogPath, 'utf8');
  const builtForVersion = readModuleVersion();
  let html = marked.parse(markdown);
  html = rewriteExternalLinks(html);

  fs.mkdirSync(OUT_DIR, { recursive: true });

  const ts = `/* eslint-disable */
// Auto-generated by scripts/generate-changelog.mjs — do not edit.

export const changelogBuiltForVersion = ${JSON.stringify(builtForVersion)};

export const changelogHtml = ${JSON.stringify(html)};
`;

  fs.writeFileSync(OUT_FILE, ts, 'utf8');
  console.log(
    `Generated changelog.ts — v${builtForVersion || '?'} → ${OUT_FILE}`,
  );
}

main();
